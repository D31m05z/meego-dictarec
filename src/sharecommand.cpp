#include "sharecommand.h"

#include <QString>
#include <QStringList>
#include <QDebug>
#include <MDataUri>
#include <maemo-meegotouch-interfaces/shareuiinterface.h>

ShareCommand::ShareCommand(QObject *parent) : QObject(parent) { }

void ShareCommand::share (QString file_name)
{
    QStringList items;

    items << file_name;
    ShareUiInterface shareIf("com.nokia.ShareUi");

    if (shareIf.isValid()) {
        qDebug() << "Signalling share-ui daemon... SHARE :" << file_name;
        shareIf.share (items);
    } else {
        qCritical() << "Invalid interface";
        return;
    }
}
